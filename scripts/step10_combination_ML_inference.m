function [p_value, perm_diff, obs_diff, ci_perm, ci_boot] = permutation_test_with_bootstrap(group1, group2)
% Permutation test with bootstrap confidence intervals - GPU accelerated
n_permutations = 1000000;
n = length(group1);
differences = group1 - group2;
obs_diff = mean(differences);

% GPU-accelerated permutation test
differences_gpu = gpuArray(differences);  % Move differences to GPU

% Generate all sign flips on GPU at once
signs_gpu = 2 * (rand(n, n_permutations, 'gpuArray') > 0.5) - 1;

% Element-wise multiplication and mean (all on GPU)
perm_differences_gpu = differences_gpu .* signs_gpu;
perm_diffs_gpu = mean(perm_differences_gpu, 1);

% Gather results back to CPU for final calculations
perm_diffs = gather(perm_diffs_gpu)';
perm_diff = mean(perm_diffs);
p_value = (sum(abs(perm_diffs) >= abs(obs_diff)) + 1) / (n_permutations + 1);

% Bootstrap CI (BCa method) - runs on CPU
try
    ci_boot = bootci(10000, {@mean, differences}, 'Type', 'bca', 'Alpha', 0.05/4);
catch
    ci_boot = bootci(10000, {@mean, differences}, 'Type', 'percentile', 'Alpha', 0.05/4);
end
ci_perm = [];
end
